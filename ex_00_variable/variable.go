package main

import (
	"fmt" // 导入 fmt 包用于格式化输出
)

func main() {
	// 1. 声明并初始化变量
	// Go语言中的变量声明使用关键字 var
	// 可以在声明变量的同时进行初始化
	var a = "initial"
	fmt.Println("变量 a 的值为：", a)

	// 2. 同时声明多个变量并进行初始化
	// 可以在一行中声明多个变量，并为它们分别赋值
	var b, c = 1, 2
	fmt.Println("变量 b 和 c 的值分别为：", b, c)

	// 3. 声明未初始化的变量
	// 如果声明了变量但没有初始化，Go 会为其赋予“零值”
	// 整型的零值为 0，字符串的零值为 ""（空字符串），布尔型的零值为 false，指针的零值为 nil
	var e int
	fmt.Println("变量 e 的默认值（零值）为：", e)

	// 4. 使用短变量声明
	// 使用 := 语法可以简洁地声明并初始化变量
	// 这种方式只能在函数内部使用
	f := "short" // 声明字符串变量 f 并初始化为 "short"
	fmt.Println("变量 f 的值为：", f)
}
